
-- 문제 1.
SELECT 
  U1.USER_ID,
  U1.USERNAME,
  U1.MANAGER_ID,
  U2.USERNAME AS "MANAGERNAME"
FROM USERS U1
JOIN USERS U2
ON U1.MANAGER_ID = U2.USER_ID
WHERE U1.USERNAME = 'choonsik'
;


-- 문제 2.
SELECT
  USER_ID,
  U.USERNAME,
  UP.FULL_NAME
FROM USERS U
JOIN USER_PROFILES UP
USING (USER_ID)
;


